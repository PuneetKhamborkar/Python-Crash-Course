# Math Module in Python
import math as Math

x = 22.9

print(Math.ceil(x))    # Rounds up to the nearest integer
print(Math.floor(x))   # Rounds down to the nearest integer
print(Math.sqrt(x))    # Square root of x
print(Math.factorial(5))  # Factorial of 5
print(Math.gcd(48, 18))   # Greatest Common Divisor of 48 and 18
print(Math.lcm(4, 44))    # Least Common Multiple of 4 and 44
print(Math.sin(Math.pi/2))  # Sine of 90 degrees
print(Math.cos(0))         # Cosine of 0 degrees
print(Math.log(100, 10))   # Logarithm of 100 to base 10
print(Math.exp(3))        # e raised to the power of 3
print(Math.radians(180))  # Convert degrees to radians
print(Math.degrees(Math.pi))  # Convert radians to degrees
